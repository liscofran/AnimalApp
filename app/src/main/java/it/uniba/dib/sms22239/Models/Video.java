package it.uniba.dib.sms22239.Models;

public class Video {

        private String mUrl;
        private String mTitle;

        public Video() {}

        public String getUrl() {
            return mUrl;
        }

        public void setUrl(String url) {
            mUrl = url;
        }

        public String getTitle() {
            return mTitle;
        }

        public void setTitle(String title) {
            mTitle = title;
        }

}
